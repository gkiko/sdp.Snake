public class Cell {
	private int x;
	private int y;
	private int value;

	public Cell(int x, int y, int value) {
		this.x = x;
		this.y = y;
		this.value = value;

	}

	public int getX() {
		return this.x;
	}

	public int getY() {
		return this.y;

	}

	public int getValue() {
		return this.value;
	}

}